    <router-view />
  </div>
</template>

<script setup>
import { onMounted } from 'vue'
import { useAuthStore } from './stores/auth'

const authStore = useAuthStore()

onMounted(() => {
  // 应用启动时检查token
  if (authStore.token) {
    console.log('用户已登录，token:', authStore.token)
  } else {
    console.log('用户未登录')
  }
})
</script>

